package section05;

public class LoopHomeWorkA2 {
	public static void main(String[] args) {
		
		for(int i=2; i<=2; i++) {
			for(int j=1; j<=9; j++) {
				System.out.println(i + "X" + j + "=" + i*j + "  " + (i+1) + "X" + j + "=" + (i+1)*j + "  " + (i+2) + "X" + j + "=" + (i+2)*j); 
			}
		System.out.println();
	    }
		
		System.out.println();
		for(int i=5; i<=5; i++) {
			for(int j=1; j<=9; j++) {
				System.out.println(i + "X" + j + "=" + i*j + "  " + (i+1) + "X" + j + "=" + (i+1)*j + "  " + (i+2) + "X" + j + "=" + (i+2)*j); 
			}
		System.out.println();
	    }
		
		System.out.println();
		for(int i=8; i<=8; i++) {
			for(int j=1; j<=9; j++) {
				System.out.println(i + "X" + j + "=" + i*j + "  " + (i+1) + "X" + j + "=" + (i+1)*j); 
			}
		System.out.println();
	    }
		
		
}
}			

		
		
		
	
	
	